# This function takes the list generated by the makeCacheMatrix function, and returns its inverse. If the inverse was computed before, it retrieves it from the cache.
cacheSolve <- function(x, ...) {
        m <- x$getinv()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setinv(m)
        m
}
